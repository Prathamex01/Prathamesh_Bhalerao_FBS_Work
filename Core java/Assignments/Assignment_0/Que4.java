 class Table {
    public static void main(String[] args) {
        int num = 5;

        for (int i = 1; i <= 10; i++) {
            int result = num * i;
            if (result % 2 == 0) {
                System.out.println(num + " x " + i + " = " + result );
            } else {
                System.out.println(num + " x " + i + " = " + result );
            }
        }
    }//main ends here
}//class Table ends here
